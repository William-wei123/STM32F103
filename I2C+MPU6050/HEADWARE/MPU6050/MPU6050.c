#include "MPU6050.h"
#include "I2C.h"

u8 MPU_Init(void) 								//初始化MPU6050
{
	u8 res=0;

	MyI2C_Init();
	MyI2C_Write_One_Byte(MPU_ADDR,MPU_PWR_MGMT1_REG,0X80);//复位MPU6050
	delay_ms(100);
	MyI2C_Write_One_Byte(MPU_ADDR,MPU_PWR_MGMT1_REG,0x00);//唤醒MPU6050
	MPU_Set_Gyro_Fsr(3);					//陀螺仪传感器,±2000dps
	MPU_Set_Accel_Fsr(0);					//加速度传感器,±2g
	MPU_Set_Rate(50);
	MyI2C_Write_One_Byte(MPU_ADDR,MPU_INT_EN_REG,0X00);	//关闭所有中断
	MyI2C_Write_One_Byte(MPU_ADDR,MPU_USER_CTRL_REG,0X00);	//I2C主模式关闭
	MyI2C_Write_One_Byte(MPU_ADDR,MPU_FIFO_EN_REG,0X00);	//关闭FIFO
	MyI2C_Write_One_Byte(MPU_ADDR,MPU_INTBP_CFG_REG,0X80);	//INT引脚低电平有效
	res=MyI2C_Read_One_Byte(MPU_ADDR,MPU_DEVICE_ID_REG);
	if(res==MPU_ADDR)//器件ID正确
	{
		MyI2C_Write_One_Byte(MPU_ADDR,MPU_PWR_MGMT1_REG,0X01);	//设置CLKSEL,PLL X轴为参考
		MyI2C_Write_One_Byte(MPU_ADDR,MPU_PWR_MGMT2_REG,0X00);	//加速度与陀螺仪都工作
		MPU_Set_Rate(50);						//设置采样率为50Hz
 	}else return 1;
	return 0;	
}



//设置MPU6050陀螺仪传感器满量程范围
//fsr:0,±250dps;1,±500dps;2,±1000dps;3,±2000dps
//返回值:0,设置成功
//    其他,设置失败 
u8 MPU_Set_Gyro_Fsr(u8 fsr)
{
	return MyI2C_Write_One_Byte(MPU_ADDR,MPU_GYRO_CFG_REG,fsr<<3);//设置陀螺仪满量程范围  
}
//设置MPU6050加速度传感器满量程范围
//fsr:0,±2g;1,±4g;2,±8g;3,±16g
//返回值:0,设置成功
//    其他,设置失败 
u8 MPU_Set_Accel_Fsr(u8 fsr)
{
	return MyI2C_Write_One_Byte(MPU_ADDR,MPU_ACCEL_CFG_REG,fsr<<3);//设置加速度传感器满量程范围  
}
//设置MPU6050的数字低通滤波器
//lpf:数字低通滤波频率(Hz)
//返回值:0,设置成功
//    其他,设置失败 
u8 MPU_Set_LPF(u16 lpf)
{
	u8 data=0;
	if(lpf>=188)data=1;
	else if(lpf>=98)data=2;
	else if(lpf>=42)data=3;
	else if(lpf>=20)data=4;
	else if(lpf>=10)data=5;
	else data=6; 
	return MyI2C_Write_One_Byte(MPU_ADDR,MPU_CFG_REG,data);//设置数字低通滤波器  
}
//设置MPU6050的采样率(假定Fs=1KHz)
//rate:4~1000(Hz)
//返回值:0,设置成功
//    其他,设置失败 
u8 MPU_Set_Rate(u16 rate)
{
	u8 data;
	if(rate>1000)rate=1000;
	if(rate<4)rate=4;
	data=1000/rate-1;
	data=MyI2C_Write_One_Byte(MPU_ADDR,MPU_SAMPLE_RATE_REG,data);	//设置数字低通滤波器
 	return MPU_Set_LPF(rate/2);	//自动设置LPF为采样率的一半
}
//得到陀螺仪值(原始值)
//gx,gy,gz:陀螺仪x,y,z轴的原始读数(带符号)
//返回值:0,成功
//    其他,错误代码
u8 MPU_Get_Gyroscope(short *gx,short *gy,short *gz)
{
    u8 buf[6],res;  
	res=MPU_Read_Len(MPU_ADDR,MPU_GYRO_XOUTH_REG,6,buf);
	if(res==0)
	{
		*gx=((u16)buf[0]<<8)|buf[1];  
		*gy=((u16)buf[2]<<8)|buf[3];  
		*gz=((u16)buf[4]<<8)|buf[5];
	} 	
    return res;;
}
//得到加速度值(原始值)
//gx,gy,gz:陀螺仪x,y,z轴的原始读数(带符号)
//返回值:0,成功
//    其他,错误代码
u8 MPU_Get_Accelerometer(short *ax,short *ay,short *az)
{
    u8 buf[6],res;  
	res=MPU_Read_Len(MPU_ADDR,MPU_ACCEL_XOUTH_REG,6,buf);
	if(res==0)
	{
		*ax=((u16)buf[0]<<8)|buf[1];  
		*ay=((u16)buf[2]<<8)|buf[3];  
		*az=((u16)buf[4]<<8)|buf[5];
	} 	
    return res;;
}
//IIC连续写
//addr:器件地址 
//reg:寄存器地址
//len:写入长度
//buf:数据区
//返回值:0,正常
//    其他,错误代码
u8 MPU_Write_Len(u8 addr,u8 reg,u8 len,u8 *buf)
{
	u8 i,temp=1; 
    MyI2C_Star(); 
	temp=MyI2C_Write_Byte((addr<<1)|0);//发送器件地址+写命令	
	if(temp)	//等待应答
	{
		MyI2C_Stop();		 
		return 1;		
	}
    MyI2C_Write_Byte(reg);	//写寄存器地址
    //MPU_IIC_Wait_Ack();		//等待应答
	for(i=0;i<len;i++)
	{
		temp=MyI2C_Write_Byte(buf[i]);	//发送数据
		if(temp)		//等待ACK
		{
			MyI2C_Stop();	 
			return 1;		 
		}		
	}    
    MyI2C_Stop();	 
	return 0;	
} 
//IIC连续读
//addr:器件地址
//reg:要读取的寄存器地址
//len:要读取的长度
//buf:读取到的数据存储区
//返回值:0,正常
//    其他,错误代码
u8 MPU_Read_Len(u8 addr,u8 reg,u8 len,u8 *buf)
{ 
 	u8 temp;
	MyI2C_Star(); 
	temp=MyI2C_Write_Byte((addr<<1)|0);//发送器件地址+写命令	
	if(temp)	//等待应答
	{
		MyI2C_Star();		 
		return 1;		
	}
    temp=MyI2C_Write_Byte(reg);	//写寄存器地址
   // MPU_IIC_Wait_Ack();		//等待应答
    MyI2C_Star();
	MyI2C_Write_Byte((addr<<1)|1);//发送器件地址+读命令	
    //MPU_IIC_Wait_Ack();		//等待应答 
	while(len)
	{
		if(len==1)*buf=MyI2C_read_Byte(1);//读数据,发送nACK 
		else *buf=MyI2C_read_Byte(0);		//读数据,发送ACK  
		len--;
		buf++; 
	}    
    MyI2C_Stop();	//产生一个停止条件 
	return 0;	
}
